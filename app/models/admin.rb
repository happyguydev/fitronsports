class Admin < ApplicationRecord
  devise :database_authenticatable, :rememberable, :validatable
  
  def active_for_authentication?
    super && self.active?
  end

  def complete_name
    "#{first_name.titleize} #{last_name.titleize}"
  end

  def deactivate
    update_attributes(active: false) 
  end

end
